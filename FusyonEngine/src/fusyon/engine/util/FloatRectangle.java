package fusyon.engine.util;

public class FloatRectangle{

    public float x;
    public float y;
    public float width;
    public float height;

    public FloatRectangle(float x, float y, float width, float height){
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
